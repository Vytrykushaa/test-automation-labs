import { abs, floor, ceil } from './functions.js';

// ==========================================
// Тести для функції abs
// ==========================================
describe('abs', () => {

  test('abs повертає позитивне число без змін', () => {
    expect(abs(5)).toBe(5);
  });

  test('abs повертає абсолютне значення від\'ємного числа', () => {
    expect(abs(-5)).toBe(5);
  });

  test('abs від нуля повертає нуль', () => {
    expect(abs(0)).toBe(0);
  });

  test('abs коректно обробляє від\'ємне дробове число', () => {
    expect(abs(-3.7)).toBe(3.7);
  });

  test('abs коректно обробляє дуже мале від\'ємне число', () => {
    expect(abs(-0.001)).toBe(0.001);
  });

});

// ==========================================
// Тести для функції floor
// ==========================================
describe('floor', () => {

  test('floor округлює дробове позитивне число вниз', () => {
    expect(floor(4.9)).toBe(4);
  });

  test('floor округлює дробове число з малою частиною вниз', () => {
    expect(floor(4.1)).toBe(4);
  });

  test('floor від\'ємного дробового числа округлює до меншого цілого', () => {
    expect(floor(-4.1)).toBe(-5);
  });

  test('floor від нуля повертає нуль', () => {
    expect(floor(0)).toBe(0);
  });

  test('floor цілого числа повертає його без змін', () => {
    expect(floor(7)).toBe(7);
  });

});

// ==========================================
// Тести для функції ceil
// ==========================================
describe('ceil', () => {

  test('ceil округлює дробове число з малою частиною вгору', () => {
    expect(ceil(4.1)).toBe(5);
  });

  test('ceil округлює дробове число з великою частиною вгору', () => {
    expect(ceil(4.9)).toBe(5);
  });

  test('ceil від\'ємного дробового числа округлює до більшого цілого', () => {
    expect(ceil(-4.9)).toBe(-4);
  });

  test('ceil від нуля повертає нуль', () => {
    expect(ceil(0)).toBe(0);
  });

  test('ceil цілого числа повертає його без змін', () => {
    expect(ceil(7)).toBe(7);
  });

});